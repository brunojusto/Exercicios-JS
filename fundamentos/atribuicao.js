const a = 7
let b = 3

b += a // Equivale b = b + a     (3 = 3 + 7 -> 10)
console.log(b)
b -= 4 // Equivale b = b - 4     (10 = 10 - 4 -> 6)
console.log(b)
b *= 2 // Equivale b = b * 2     (6 = 6 * 2 -> 12)
console.log(b)
b /= 2 // Equivale b = b / 2     (12 = 12 / 2 -> 6)
console.log(b)
b %= 2 // Equivale b = b % 2     (6 = 6 % 2 -> 0) 
console.log(b)